// Form interactions with Formspree email functionality
function initFormInteractions() {
    const contactForm = document.getElementById('booking-form');
    const newsletterForm = document.querySelector('.newsletter-form');
    const formStatus = document.getElementById('form-status');

    if (contactForm) {
        // Set minimum date to today
        const dateInput = document.getElementById('date');
        if (dateInput) {
            const today = new Date().toISOString().split('T')[0];
            dateInput.setAttribute('min', today);
        }

        // Set reply-to field from email input
        const emailInput = document.getElementById('email');
        const replytoField = document.getElementById('replyto-field');
        if (emailInput && replytoField) {
            emailInput.addEventListener('input', () => {
                replytoField.value = emailInput.value;
            });
        }

        contactForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            
            const submitBtn = contactForm.querySelector('button[type="submit"]');
            const originalText = submitBtn.innerHTML;
            
            // Show loading state
            submitBtn.disabled = true;
            submitBtn.innerHTML = '<span>Sending...</span>';
            
            try {
                const formData = new FormData(contactForm);
                const response = await fetch(contactForm.action, {
                    method: 'POST',
                    body: formData,
                    headers: {
                        'Accept': 'application/json'
                    }
                });
                
                if (response.ok) {
                    // Success
                    showFormStatus('success', 'Thank you! Your appointment request has been sent. We will contact you shortly to confirm.');
                    contactForm.reset();
                    
                    // Also show notification toast
                    showNotification('✅ Appointment request sent successfully! Check your email for confirmation.');
                } else {
                    // Error
                    const data = await response.json();
                    if (data.errors) {
                        showFormStatus('error', data.errors.map(error => error.message).join(', '));
                    } else {
                        showFormStatus('error', 'Oops! There was a problem submitting your form. Please try again.');
                    }
                }
            } catch (error) {
                showFormStatus('error', 'Oops! There was a problem submitting your form. Please check your internet connection and try again.');
            } finally {
                submitBtn.disabled = false;
                submitBtn.innerHTML = originalText;
            }
        });
    }

    if (newsletterForm) {
        newsletterForm.addEventListener('submit', (e) => {
            e.preventDefault();
            showNotification('Thank you for subscribing to our newsletter!');
            newsletterForm.reset();
        });
    }

    // Floating label animation enhancement
    const formInputs = document.querySelectorAll('.form-group input, .form-group textarea, .form-group select');
    formInputs.forEach(input => {
        input.addEventListener('focus', () => {
            input.parentElement.classList.add('focused');
        });
        
        input.addEventListener('blur', () => {
            if (!input.value) {
                input.parentElement.classList.remove('focused');
            }
        });
    });
}

