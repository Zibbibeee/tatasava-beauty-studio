// Language Switcher Functions
function initLanguageSwitcher() {
    const langToggle = document.getElementById('langToggle');
    const langSelect = document.querySelector('.lang-select');
    const langOptions = document.querySelectorAll('.lang-option');
    const langCode = document.querySelector('.lang-code');
    const langFlag = document.querySelector('.lang-flag');
    
    // Toggle dropdown
    if (langToggle && langSelect) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            langSelect.classList.toggle('active');
        });
        
        // Close dropdown when clicking outside
        document.addEventListener('click', (e) => {
            if (!langSelect.contains(e.target)) {
                langSelect.classList.remove('active');
            }
        });
    }
    
    // Language option clicks
    langOptions.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            const lang = btn.dataset.lang;
            if (lang && lang !== currentLang) {
                setLanguage(lang);
                
                // Update language display
                const langData = {
                    'en': { code: 'EN', flag: '🇬🇧' },
                    'fr': { code: 'FR', flag: '🇫🇷' },
                    'uk': { code: 'UK', flag: '🇺🇦' }
                };
                
                if (langCode) langCode.textContent = langData[lang].code;
                if (langFlag) langFlag.textContent = langData[lang].flag;
                
                // Close dropdown
                if (langSelect) langSelect.classList.remove('active');
                
                // Save to localStorage
                localStorage.setItem('tatasava-lang', lang);
            }
        });
    });
}

function setLanguage(lang) {
    if (!translations[lang]) return;
    
    currentLang = lang;
    const t = translations[lang];
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const value = getNestedValue(t, key);
        if (value) {
            el.innerHTML = value;
        }
    });
    
    // Update all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        const value = getNestedValue(t, key);
        if (value) {
            el.placeholder = value;
        }
    });
    
    // Update select options with data-i18n
    document.querySelectorAll('select option[data-i18n]').forEach(option => {
        const key = option.dataset.i18n;
        const value = getNestedValue(t, key);
        if (value) {
            option.textContent = value;
        }
    });
    
    // Update document lang attribute
    document.documentElement.lang = lang === 'uk' ? 'uk' : lang;
    
    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

function getNestedValue(obj, path) {
    return path.split('.').reduce((current, key) => {
        return current && current[key] !== undefined ? current[key] : null;
    }, obj);
}

// Gallery Filter Functionality
function initGalleryFilter() {
    const filterButtons = document.querySelectorAll('.filter-btn');
    const galleryItems = document.querySelectorAll('.gallery-item');

    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            const filter = button.dataset.filter;

            // Update active button
            filterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');

            // Filter gallery items
            galleryItems.forEach(item => {
                const category = item.dataset.category;

                if (filter === 'all' || category === filter) {
                    item.classList.remove('hide');
                } else {
                    item.classList.add('hide');
                }
            });
        });
    });
}

